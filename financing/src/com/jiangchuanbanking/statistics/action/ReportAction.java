package com.jiangchuanbanking.statistics.action;

import com.jiangchuanbanking.base.service.IBaseService;
import com.opensymphony.xwork2.ActionSupport;

@SuppressWarnings("serial")
public class ReportAction extends ActionSupport{
	private String pact_no;
	private String cif_name;
	private IBaseService baseService;
	/**
	 * 月度付款汇总表
	 * @return
	 */
	public String monthPaySum(){
		return "monthPaySum";
	}
	/**
	 * 负债统计表
	 * @return
	 */
	public String liabilitySum(){
		return "liabilitySum";
	}
	/**
	 * 提成计算基础表
	 * @return
	 */
	public String percentCalcul(){
		return "percentCalcul";
	}
	public String getPact_no() {
		return pact_no;
	}
	public void setPact_no(String pact_no) {
		this.pact_no = pact_no;
	}
	public String getCif_name() {
		return cif_name;
	}
	public void setCif_name(String cif_name) {
		this.cif_name = cif_name;
	}
	public IBaseService getBaseService() {
		return baseService;
	}
	public void setBaseService(IBaseService baseService) {
		this.baseService = baseService;
	}
	
	
}
